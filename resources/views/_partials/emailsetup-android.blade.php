    <h3>Set Up Email on Your Android-based Device</h3>
    
    <p>Open your devices email application</p>
    <p>If you already have an email account set up, press <b>Menu</b> and tap <b>Accounts</b>. Press <b>Menu</b> again and tap <b>Add account</b></p>
    <br>
    <img src="/public/assets/images/emailsetup/android/setup1.jpg" alt="">
    <br><br>
    
    <p>Type your <b>Email address</b> and <b>Password</b>, and click <b>Next</b>.</p>
    <br>
    <img src="/public/assets/images/emailsetup/android/setup2.jpg" alt="">
    <br><br>
    
    <p>If you have IMAP, tap <b>IMAP</b>. If you're not sure, tap <b>POP3</b>.</p>
    <br>
    <img src="/public/assets/images/emailsetup/android/setup3.jpg" alt="">
    <br><br>

    <p>On the next screen, enter your User name, password (For Incoming server settings) and IMAP Server.<br>
        Port: Either 993 or 143 (very infrequently 585)<br>
        Security type: SSL<br>
        Tap on next<br>
    </p>
    <br>
    <img src="/public/assets/images/emailsetup/android/setup4.jpg" alt="">
    <br><br>

    <p>
        Use these settings for your incoming server, depending on the type of email you have:
