    <h3>Mac Mail</h3>
    
    <p>To begin with, click on the Apple Icon Menu on the top left of your screen and select the <strong>System Preferences...</strong> submenu.</p>
    <img src="/public/assets/images/emailsetup/mac-mail/image001.jpg" alt=""><br>
    <br><br>
    
    <p>You will be taken to your System Preferences screen where you can adjust various options of your Mac OS. Select the <strong>Internet Accounts</strong> link to proceed. </p>
    <img src="/public/assets/images/emailsetup/mac-mail/image002.jpg" alt=""><br>
    <br><br>
    
    <p>Here, you will see a list of all the different accounts you have registered including iCloud, FaceBook, Vimeo, etc. Scroll down and select the <strong>Add Other Account...</strong> option. </p>
    <img src="/public/assets/images/emailsetup/mac-mail/image003.jpg" alt=""><br>
    <br><br>
    
    <p>Next, select to add a <strong>Mail account</strong> on the next screen. </p>
    <img src="/public/assets/images/emailsetup/mac-mail/image004.jpg" alt=""><br>
    <br><br>
    
    <p>A popup will appear asking you to fill in your name and the Email address that you want to register with Mac Mail. Note, that people you send emails too will receive them from the name you enter here. This option, however, can be changed later on. Finally, click <strong>Sign</strong> in to proceed.</p>
    <img src="/public/assets/images/emailsetup/mac-mail/image004.jpg" alt=""><br>
    <br><br>
    
    <p>Now, you have to fill in the server settings for your Email account:</p>
    • &nbsp&nbsp <strong>Email Address:</strong> - should be pre-filled with the value you've entered in the previous screen;<br>
    • &nbsp&nbsp <strong>Username:</strong> - enter your full email address as username. It will be used both for the incoming and outgoing connection;<br>
    • &nbsp&nbsp <strong>Password:</strong> - enter the password for your email account;<br>
    • &nbsp&nbsp <strong>Account Type:</strong> - it's set to IMAP by default. Unless you specifically want to use POP3, please leave it that way;<br>
    • &nbsp&nbsp <strong>Incoming Mail Server:</strong> - add <strong>mail</strong>.yourdomain.com unless you have a specific MX records configuration for your domain name;<br>
    • &nbsp&nbsp <strong>Incoming Mail Server:</strong> - same as the incoming server;<br>
    <br><br>
    
    <p>Once you fill in all the details, press <strong>Sign In</strong> to proceed.</p>
    <img src="/public/assets/images/emailsetup/mac-mail/image005.jpg" alt=""><br>
    <br><br>
    
    <p>Finally, you will be asked which programs you want to use this account with. The available options depend on the applications you have installed on your account.</p>
    <img src="/public/assets/images/emailsetup/mac-mail/image006.jpg" alt=""><br>
    <br><br>
    
    <p>Done</p>
    
