    <h3>IPad / IPhone / IPod</h3>
    <br>
    <h4><strong>Adding Manually:</strong></h4>
    
    <p>Go to Settings > Mail > Add Account.</p>
    <img src="/public/assets/images/emailsetup/iphone/image001.png" alt="">
    <br><br>
    
    <p>If you see your email provider, tap it to add your account automatically. If you don't see your email provider, tap <a href="https://support.apple.com/en-ca/HT201320#manual"> Other to add your account manually</a> </p>
    <br>
    
    <p>Tap Add Account, tap Other, then tap Add Mail Account.</p>
    <img src="/public/assets/images/emailsetup/iphone/image002.png" alt="">
    <br><br>
    
    <p>Enter your name, email address, password, and a description for your account.</p>
    <img src="/public/assets/images/emailsetup/iphone/image003.jpg" alt=""><br><br>
    <img src="/public/assets/images/emailsetup/iphone/image004.png" alt=""><br>
    <br><br>
    
    <p>Tap Next. Mail will try to find the email settings and finish your account setup. If Mail finds your email settings, tap Done to complete your account setup.</p>
    <br><br>
    <b>If Mail can't find your email settings, you need to enter them manually. After you enter your email address and password, tap Next, then follow these steps:</b>
    <br><br>
    
    <p>Choose IMAP or POP for your new account. If you arenâ€™t sure which one to choose, contact your email provider.</p>
    <p>Enter the information for Incoming Mail Server and Outgoing Mail Server. Then tap Next. If you don't have this information, try to <a href="https://www.apple.com/support/mail-settings-lookup/">look it up</a> or <a href="https://support.apple.com/kb/HT203995">contact your email provider</a></p>
    <p>If your email settings are correct, tap Save to finish. If the email settings are incorrect, you'll be asked to edit them.</p>
    

