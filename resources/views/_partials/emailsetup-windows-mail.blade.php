    <h3>Windows Live Mail</h3>
    <br>
    <strong>Steps on How to Set up</strong>
    <br><br>
    
    <p>Open Windows Live Mail.</p>

    <p>You will be prompted to create an email account.</p>
    <img src="/public/assets/images/emailsetup/windows-mail/image001.png" alt=""><br>
    <br><br>
    
    <p>Next, enter your Account Settings.</p>
    <img src="/public/assets/images/emailsetup/windows-mail/image002.png" alt=""><br>
    <br>
        <div>
            o    &nbsp;&nbsp; My incoming mail server is a (select IMAP or POP) server. We suggest IMAP. <a href="https://mediatemple.net/community/products/all/204643630">Learn more here</a>.<br>
            o    &nbsp;&nbsp; Incoming server: xxxx-xxxx.accessdomain.commail.mt-example.com<br>
            o    &nbsp;&nbsp; Port: 143 for IMAP or 110 for POP<br>
            o    &nbsp;&nbsp; This server requires a secure connection (SSL) should be unchecked.<br>
            o    &nbsp;&nbsp; Log on using: Clear text authentication.<br>
            o    &nbsp;&nbsp; Login ID: KB-User@mt-example.com<br>
            o    &nbsp;&nbsp; Outgoing Server: xxxx-xxxx.accessdomain.commail.mt-example.com<br>
            o    &nbsp;&nbsp; Port: 25 or 587<br>
            o    &nbsp;&nbsp; This server requires a secure connection (SSL) should be unchecked.<br>
            o    &nbsp;&nbsp; Please check the box for My outgoing server requires authentication.<br>
        
        </div>
    <br>
    
    <p>You should now get a success message. Please continue:</p>
    <img src="/public/assets/images/emailsetup/windows-mail/image003.png" alt=""><br>
    <br><br>
    
    <p>You will now get a message that your folders are downloading:</p>
    <img src="/public/assets/images/emailsetup/windows-mail/image004.png" alt=""><br>
    <br><br>
    
    <p>Please verify that your folders are correct, then click OK:</p>
    <img src="/public/assets/images/emailsetup/windows-mail/image005.png" alt=""><br>
    <br><br>
    
    <p>You should now be able to send and receive emails:</p>
    <img src="/public/assets/images/emailsetup/windows-mail/image006.png" alt=""><br>
    <br><br>
    
