<div class="content">
    <div><b>To integrate a Post Office Protocol (POP) or Internet Message Access Protocol (IMAP) email account through advanced integration using the web login, complete the following steps:</b></div><br><br>
    <p>Log in to the BlackBerry Internet Service account with the user name and password.</p>
    <p>Click <strong>Set Up Email</strong> to begin the integration process.</p>
    <p>Type the email address and the password in the <strong>Email address</strong> and <strong>Email Password</strong> fields, and click <strong>Next</strong>.</p>
    <p>If there is an issue with integrating the email account, it will prompt to provide further information.</p>
    <p>Manually provide the settings to integrate the email account by clicking the <strong>provide additional settings</strong> link.</p>
    <p>Choose <strong>provide additional settings</strong>, identify the integration as either <strong>POP</strong> or <strong>IMAP</strong> and provide the advanced settings.</p>
    <br>
    <strong>Note:</strong> In some cases, <strong>I will provide the settings</strong> may not appear. To force this option to appear, enter an invalid email address such as user@domain.com and an invalid password. Be sure to re-type the correct email and password once the advanced settings are made available.
    <br><br>
    <p>Click Next.</p>
    <p>When choosing to provide additional settings, select the <strong>Automatically detect my POP/IMAP email settings from Microsoft® Outlook®/Outlook® Express on my computer</strong> option and click <strong>Install</strong> and <strong>Detect</strong>.<br>
    <strong>Note:</strong> This feature requires Windows Internet Explorer.
    </p>
    <p>Confirm the populated details and click <strong>Next</strong>.</p>
    A configuration window will appear if the email account was successfully added as a POP or IMAP integration.
    <br><br>
    <p>Click <strong>Finish</strong> to return to the BlackBerry Internet Service Home screen, or click <strong>Add Another Account</strong> to integrate another email address.</p>
